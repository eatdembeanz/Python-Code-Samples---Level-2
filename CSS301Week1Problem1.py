#Problem 1 - Writes a function that asks for user input, and converts degrees Celsius to degrees Fahrenheit


DegreesC = int(input("Please enter a number in degrees Celsius."))

DegreesF = (DegreesC * (9/5)) + 32

print("Degrees Celsius:",DegreesC)
print("Degrees Fahrenheit:",DegreesF)
